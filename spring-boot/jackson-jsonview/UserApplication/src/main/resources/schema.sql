CREATE TABLE IF NOT EXISTS USER( ID INTEGER  GENERATED BY DEFAULT AS IDENTITY (START WITH 1000), FIRST_NAME VARCHAR(45) NOT NULL, LAST_NAME VARCHAR(45) NOT NULL, DOB VARCHAR(45) NOT NULL, ADDRESS VARCHAR(100), SUBURB VARCHAR(45), CITY VARCHAR(45), IS_INTERNAL_USER BOOLEAN, LOGIN_ID VARCHAR(45), LOGIN_PASSWORD VARCHAR(45), SSN_NUMBER VARCHAR(45), CREATED_BY VARCHAR(45), CREATED_DATE DATE, UPDATED_BY VARCHAR(45), UPDATED_DATE DATE, PRIMARY KEY(ID));

